// Arrays 001
#include <stdio.h>
int main(void)
{
    /*
    If the Array size is omitted from a definition with an initializer list,
    the number of elements in the Array will be the number of elements in the
    initializer list .
    */
    int arr[] = {1 , 2 , 3 } ; // array size will = 3
    return 0 ;
}
